/**
 * AssignmentOperators
 */
public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 4;
        a += 7;
        System.out.println(a);
        a -= 7;
        System.out.println(a);
        a *= 7;
        System.out.println(a);
        a /= 7;
        System.out.println(a);
        a <<= 7;
        System.out.println(a);
        a >>= 7;
        System.out.println(a);
        a &= 7;
        System.out.println(a);
        a |= 7;
        System.out.println(a);
    }
}